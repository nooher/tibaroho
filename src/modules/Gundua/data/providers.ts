/**
 * Initials avatar — a deterministic SVG data-URL chip from the provider's
 * name. Photos are not real; this keeps the seed dignified.
 */
export function initialsOf(name: string): string {
  const parts = name.replace(/^(Dr\.?|Prof\.?|Bw\.?|Bi\.?|Mch\.?|Sh\.?)\s+/i, '').split(/\s+/)
  const a = parts[0]?.[0] ?? '?'
  const b = parts[parts.length - 1]?.[0] ?? ''
  return (a + b).toUpperCase()
}

